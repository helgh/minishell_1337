#include "../minishell.h"

void	put_str(char *str, int fd)
{
	int	i;

	i = -1;
	if (!str)
		return ;
	write(fd, "M_H: ", 5);
	while (str[++i])
		write(fd, &str[i], 1);
	write(2, ": ", 2);
}
